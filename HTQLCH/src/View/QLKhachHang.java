    private void fillDataToTable() {
        khachHangs = new KhachHang_DAO().getListKhachHang();
        model.setRowCount(0);
        for (KhachHang s : khachHangs) {
            model.addRow(new Object[]{
                tableKH.getRowCount() + 1, s.getMaKH(), s.getTenKH(), formatDate.format(s.getNgaySinh()), s.getGioiTinh(), s.getDiaChi(), s.getSdt(), s.getLoaiKH().getTenLKH()
            });
        }
    }

    private void getCbxLoaiKH() {
        cbxModel = (DefaultComboBoxModel) cbxLoaiKH.getModel();
        for (LoaiKhachHang k : daoLKH.getList()) {
            cbxModel.addElement(k);
        }
    }

