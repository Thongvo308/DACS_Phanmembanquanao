    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String name = txtTimKiem.getText();
        if (name.length() == 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin tìm kiếm!");
        } else if (name.length() > 0) {
            khachHangs = new KhachHang_DAO().findByName(name);
            model.setRowCount(0);
            for (KhachHang s : khachHangs) {
                model.addRow(new Object[]{
                    tableKH.getRowCount() + 1, s.getMaKH(), s.getTenKH(), formatDate.format(s.getNgaySinh()), s.getGioiTinh(), s.getDiaChi(), s.getSdt(), s.getLoaiKH().getTenLKH()
                });
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

