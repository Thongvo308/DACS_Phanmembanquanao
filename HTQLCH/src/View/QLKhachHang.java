    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            if (validateForm() == true) {
                KhachHang s = new KhachHang();
                s.setMaKH(txtMaKH.getText());
                s.setTenKH(txtTenKH.getText());
                s.setNgaySinh(formatDate.parse(txtNgaySinh.getText()));
                String gt = "";
                if (rbtnNam.isSelected()) {
                    gt = "Nam";
                } else {
                    gt = "Nữ";
                }
                s.setGioiTinh(gt);
                s.setDiaChi(txtDiaChi.getText());
                s.setSdt(txtSDT.getText());
                LoaiKhachHang l = daoLKH.getByName(cbxLoaiKH.getSelectedItem().toString());
                s.setLoaiKH(l);
                new KhachHang_DAO().insert(s);
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                fillDataToTable();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            selectedIndexRow = tableKH.getSelectedRow();
            KhachHang k = khachHangs.get(selectedIndexRow);
            if (validateForm() == true) {
                int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn sửa?");
                if (reply == JOptionPane.YES_OPTION) {
                    new KhachHang_DAO().delete(k.getId());
                    KhachHang s = new KhachHang();
                    s.setMaKH(txtMaKH.getText());
                    s.setTenKH(txtTenKH.getText());
                    s.setNgaySinh(formatDate.parse(txtNgaySinh.getText()));
                    String gt = "";
                    if (rbtnNam.isSelected()) {
                        gt = "Nam";
                    } else {
                        gt = "Nữ";
                    }
                    s.setGioiTinh(gt);
                    s.setDiaChi(txtDiaChi.getText());
                    s.setSdt(txtSDT.getText());
                    LoaiKhachHang l = daoLKH.getByName(cbxLoaiKH.getSelectedItem().toString());
                    s.setLoaiKH(l);
                    new KhachHang_DAO().insert(s);
                    JOptionPane.showMessageDialog(this, "Sửa thành công!");
                    fillDataToTable();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng dữ liệu để sửa!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        selectedIndexRow = tableKH.getSelectedRow();
        KhachHang s = khachHangs.get(selectedIndexRow);
        if (selectedIndexRow == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng dữ liệu để xoá!");
        } else {
            int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá?");
            if (reply == JOptionPane.YES_OPTION) {
                new KhachHang_DAO().delete(s.getId());
                JOptionPane.showMessageDialog(this, "Xoá thành công!");
                fillDataToTable();
            } else {
                return;
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

