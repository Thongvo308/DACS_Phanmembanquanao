    private void btnLuuCTHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuCTHDActionPerformed
        // TODO add your handling code here:
        try {
            int tongSL = 0;
            ChiTietHoaDon c = new ChiTietHoaDon();
            HoaDon idHD = daoHD.getByID(Integer.parseInt(txtMaHD.getText()));
            c.setHoaDon(idHD);
            int idCTSP = 0;
            for (int i = 0; i < tableCTSP.getRowCount(); i++) {
                idCTSP = (int) tableCTSP.getValueAt(i, 0);
            }
            c.setIdSanPham(idCTSP);
            for (int i = 0; i < tableCTSP.getRowCount(); i++) {
                tongSL += Integer.parseInt(tableCTSP.getValueAt(i, 3).toString());
            }
            c.setSoLuong(tongSL);
            new ChiTietHD_DAO().insert(c);
            JOptionPane.showMessageDialog(this, "Lưu hoá đơn thành công!");
            btnInHD.setEnabled(true);
            btnLuuCTHD.setEnabled(false);

            ChiTietHoaDonSanPham ct = new ChiTietHoaDonSanPham();
            ct.setIdHD(Integer.parseInt(txtMaHD.getText()));
            ct.setIdCTSP(idCTSP);
            ct.setThanhTien(Integer.parseInt(txtThanhTien.getText()));
            new ChiTietHDSP_DAO().insert(ct);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuCTHDActionPerformed

