    private void FillDataToTableHD() {
        hoaDons = new HoaDon_DAO().getListHoaDon();
        modelHD.setRowCount(0);
        for (HoaDon s : hoaDons) {
            modelHD.addRow(new Object[]{
                tableHD.getRowCount() + 1, s.getId(), formatDate.format(s.getNgayDat()), s.getThanhTien()
            });
        }
    }

    private void FillDataToTableSP() {
        sanPhams = new SanPham_DAO().getListSanPham();
        modelSP.setRowCount(0);
        for (SanPham s : sanPhams) {
            modelSP.addRow(new Object[]{
                tableSP.getRowCount() + 1, s.getMaSP(), s.getTenSP(), s.getLoaiSP(), s.getKichCo(), s.getMauSac(), s.getSoluong(), s.getGiaBan()
            });
        }
    }

