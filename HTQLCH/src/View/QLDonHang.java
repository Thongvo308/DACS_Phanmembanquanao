    private void btnInHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInHDActionPerformed
        // TODO add your handling code here:       
        String jdbc_driver = "com.mysql.cj.jdbc.Driver";
        String url = "jdbc:mysql://localhost:3306/store_system_managerment";
        String user = "root";
        String password = "";
        File reportFile = new File(".");
        String dirr = "";
        try {
            Class.forName(jdbc_driver);
            Connection conn = (Connection) DriverManager.getConnection(url, user, password);
            Statement st = conn.createStatement();
            String sql = "SELECT *\n"
                    + "FROM `product details` \n"
                    + "INNER JOIN `product invoice details` ON `product details`.`id` = `product invoice details`.`id product details`";
            dirr = reportFile.getCanonicalPath() + "/src/Report/";
            JasperDesign design = JRXmlLoader.load(dirr + "report_HD.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(design);
            ResultSet rs = st.executeQuery(sql);
            JRResultSetDataSource rsDataSource = new JRResultSetDataSource(rs);
            JasperPrint jp = JasperFillManager.fillReport(jr, new HashMap(), rsDataSource);
            JasperViewer.viewReport(jp);
            btnInHD.setEnabled(false);
        } catch (ClassNotFoundException | SQLException | IOException | JRException ex) {
            JOptionPane.showMessageDialog(null, "\nKhong in duoc\n" + ex, "Loi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInHDActionPerformed

    private boolean validateForm() {
        if (txtMaSP.getText().equals("Mã sản phẩm")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn sản phẩm!");
            return false;
        }
        if (txtSoLuong.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập số lượng!");
            return false;
        }
        try {
            Integer.parseInt(txtSoLuong.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Số lượng nhập phải là số, không được chứa ký tự!");
            return false;
        }
        try {
            int tmp = Integer.parseInt(txtSoLuong.getText());
            if (tmp == 0) {
                JOptionPane.showMessageDialog(this, "Số lượng bạn nhập vào phải lớn hơn 0");
                return false;
            }
        } catch (Exception e) {
        }
        return true;
    }

