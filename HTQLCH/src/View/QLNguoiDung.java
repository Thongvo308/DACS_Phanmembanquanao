    public void refresh() {
        txtMatKhau.setText("");
        txtTenTK.setText("");
        txtTenTK.setEnabled(true);
        cbxMaNV.setEnabled(true);
    }

    private boolean validateForm() {
        if (txtMatKhau.getText().equals("") || txtTenTK.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đầy đủ thông tin!");
            return false;
        }
        return true;
    }

    private void fillDataToTable() {
        taiKhoans = new TaiKhoan_DAO().getTaiKhoan();
        model.setRowCount(0);
        for (TaiKhoan s : taiKhoans) {
            model.addRow(new Object[]{
                tableTK.getRowCount() + 1, s.getTenTK(), s.getMatKhau(), s.getNhanVien().getMaNV(), s.getLoaiTaiKhoan().getTenLTK()
            });
        }

    }

    public void getCbxNhanVien() {
        cbxModel = (DefaultComboBoxModel) cbxMaNV.getModel();
        for (NhanVien hsx : daoNV.getListNhanVien()) {
            cbxModel.addElement(hsx);
        }
    }

    public void getCbxLoaiTK() {
        cbxModel = (DefaultComboBoxModel) cbxQuyen.getModel();
        for (LoaiTaiKhoan lsp : daoLTK.getListLoaiTK()) {
            cbxModel.addElement(lsp);
        }
    }

