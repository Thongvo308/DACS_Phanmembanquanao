    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            if (validateForm() == true) {
                TaiKhoan s = new TaiKhoan();
                s.setTenTK(txtTenTK.getText());
                s.setMatKhau(txtMatKhau.getText());
                NhanVien nv = daoNV.getIDNhanVien(cbxMaNV.getSelectedItem().toString());
                s.setNhanVien(nv);
                LoaiTaiKhoan ltk = daoLTK.getByName(cbxQuyen.getSelectedItem().toString());
                s.setLoaiTaiKhoan(ltk);
                new TaiKhoan_DAO().insert(s);
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                fillDataToTable();
                refresh();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        selectedIndexRow = tableTK.getSelectedRow();
        TaiKhoan s = taiKhoans.get(selectedIndexRow);
        if (selectedIndexRow >= 0) {
            int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá?");
            if (reply == JOptionPane.YES_OPTION) {
                new TaiKhoan_DAO().delete(s.getTenTK());
                JOptionPane.showMessageDialog(this, "Xoá thành công!");
                fillDataToTable();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Không có tài khoản để xoá!\nVui lòng chọn tài khoản hoặc thêm tài khoản!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThayMKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThayMKActionPerformed
        // TODO add your handling code here:
        try {
            selectedIndexRow = tableTK.getSelectedRow();
            TaiKhoan s = taiKhoans.get(selectedIndexRow);
            if (selectedIndexRow >= 0) {
                int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn thay đổi?");
                if (reply == JOptionPane.YES_OPTION) {
                    new TaiKhoan_DAO().delete(s.getTenTK());
                    TaiKhoan t = new TaiKhoan();
                    t.setTenTK(txtTenTK.getText());
                    t.setMatKhau(txtMatKhau.getText());
                    NhanVien nv = daoNV.getIDNhanVien(cbxMaNV.getSelectedItem().toString());
                    t.setNhanVien(nv);
                    LoaiTaiKhoan ltk = daoLTK.getByName(cbxQuyen.getSelectedItem().toString());
                    t.setLoaiTaiKhoan(ltk);
                    new TaiKhoan_DAO().insert(t);
                    JOptionPane.showMessageDialog(this, "Thay đổi mật khẩu thành công!");
                    fillDataToTable();
                    refresh();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng dữ liệu để tiến hành thay đổi!");
            }
        } catch (Exception e) {
            e.printStackTrace();      
        }
    }//GEN-LAST:event_btnThayMKActionPerformed

