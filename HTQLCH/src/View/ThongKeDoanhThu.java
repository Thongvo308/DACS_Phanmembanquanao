            InputStream in = new FileInputStream("C:\\Data\\WorkSpace\\Java\\ProjectAtSchool\\HTQLCH\\src\\Report\\report_DT.jrxml");
            JasperDesign jd = JRXmlLoader.load(in);
            String sql = "select * from `invoice`";
            JRDesignQuery newQuery = new JRDesignQuery();
