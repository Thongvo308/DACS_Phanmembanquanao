    private void fillDataToTable() {
        hoaDons = new HoaDon_DAO().getListHoaDon();
        model.setRowCount(0);
        for (HoaDon n : hoaDons) {
            model.addRow(new Object[]{
                n.getId(), formatDate.format(n.getNgayDat()), n.getThanhTien()
            });
        }
    }

    private void total(){
        DecimalFormat x = new DecimalFormat("###,###,###");
        int tong = 0;
        for (int i = 0; i < table.getRowCount(); i++) {
            tong+=Integer.parseInt(table.getValueAt(i, 2).toString());
        }
        txtTongDT.setText(x.format(tong)+" "+"VND");
    }
    
