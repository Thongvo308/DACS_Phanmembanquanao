    private void btnLuuPNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuPNActionPerformed
        // TODO add your handling code here:
        try {
            if (validateForm() == true) {
                int tong = 0;
                int gia = Integer.parseInt(txtGiaNhap.getText());
                int sl = Integer.parseInt(txtSoLuong.getText());
                PhieuNhap s = new PhieuNhap();
                NhanVien maNV = daoNV.getIDNhanVienKH(cbxMaNV.getSelectedItem().toString());
                s.setNhanVien(maNV);
                s.setNhaPP(Integer.parseInt(txtIDNPP.getText()));
                s.setNgayLap(formatDate.parse(txtNgay.getText()));
                tong = gia * sl;
                s.setTongTien(tong);
                new PhieuNhap_DAO().insert(s);
                JOptionPane.showMessageDialog(this, "Lưu thành công!");
                FillDataToTablePN();
                disableForm();
                btnLuuCTPN.setEnabled(true);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuPNActionPerformed

    private void btnXoaPNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaPNActionPerformed
        // TODO add your handling code here:
        try {
            selectedIndexRow = tablePN.getSelectedRow();
            PhieuNhap c = phieuNhaps.get(selectedIndexRow);
            if (selectedIndexRow >= 0) {
                int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá?");
                if (reply == JOptionPane.YES_OPTION) {
                    new PhieuNhap_DAO().delete(c.getId());
                    JOptionPane.showMessageDialog(this, "Đã xoá phiếu nhập!");
                    FillDataToTablePN();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Không có phiếu nhập để xoá!\nVui lòng chọn phiếu nhập hoặc thêm phiếu nhập!");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnXoaPNActionPerformed

