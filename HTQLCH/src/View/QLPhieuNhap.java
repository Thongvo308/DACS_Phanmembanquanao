    private void btnTimKiemNPPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemNPPActionPerformed
        // TODO add your handling code here:
        String name = txtTimKiemNPP.getText();
        if (name.length() == 0) {
            FillDataToTableNPP();
        } else if (name.length() > 0) {
            nhaPhanPhois = new NhaPP_DAO().findByName(name);
            modelNPP.setRowCount(0);
            for (NhaPhanPhoi s : nhaPhanPhois) {
                modelNPP.addRow(new Object[]{
                    s.getId(), s.getMaNPP(), s.getTenNPP(), s.getDiaChi(), s.getSdt(), s.getEmail()
                });
            }
        }
    }//GEN-LAST:event_btnTimKiemNPPActionPerformed

