    private void tableNPPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableNPPMouseClicked
        // TODO add your handling code here:
        txtMaNPP.setEnabled(false);
        selectedIndexRow = tableNPP.getSelectedRow();
        txtMaNPP.setText(model.getValueAt(selectedIndexRow, 1).toString());
        txtTenNPP.setText(model.getValueAt(selectedIndexRow, 2).toString());
        txtDiaChi.setText(model.getValueAt(selectedIndexRow, 3).toString());
        txtSDT.setText(model.getValueAt(selectedIndexRow, 4).toString());
        txtEmail.setText(model.getValueAt(selectedIndexRow, 5).toString());
    }//GEN-LAST:event_tableNPPMouseClicked

    private boolean validateForm() {
        if (txtMaNPP.getText().equals("") || txtTenNPP.getText().equals("") || txtDiaChi.getText().equals("")
                || txtSDT.getText().equals("") || txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đầy đủ thông tin!");
            return false;
        }
        if (!(Pattern.matches("0\\d{9,11}", txtSDT.getText()))) {
            JOptionPane.showMessageDialog(this, "Bạn nhập sai số điện thoại!");
            return false;
        }
        if (!(Pattern.matches("^[a-zA-Z0-9_!#$%&'*+/=?`{|}~^.-]+@[a-zA-Z0-9.-]+$", txtEmail.getText()))) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đúng email!");
            return false;
        }
        return true;
    }

    private void resetForm() {
        txtMaNPP.setText("");
        txtTenNPP.setText("");
        txtDiaChi.setText("");
        txtSDT.setText("");
        txtEmail.setText("");
        txtMaNPP.setEnabled(true);
    }

    private void fillDataToTable() {
        nhaPhanPhois = new NhaPP_DAO().getListNhaPP();
        model.setRowCount(0);
        for (NhaPhanPhoi n : nhaPhanPhois) {
            model.addRow(new Object[]{
                tableNPP.getRowCount() + 1, n.getMaNPP(), n.getTenNPP(), n.getDiaChi(), n.getSdt(), n.getEmail()
            });
        }
    }

