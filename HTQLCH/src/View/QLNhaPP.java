    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            if (validateForm() == true) {
                NhaPhanPhoi n = new NhaPhanPhoi();
                n.setMaNPP(txtMaNPP.getText());
                n.setTenNPP(txtTenNPP.getText());
                n.setDiaChi(txtDiaChi.getText());
                n.setSdt(txtSDT.getText());
                n.setEmail(txtEmail.getText());
                new NhaPP_DAO().insert(n);
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                fillDataToTable();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        selectedIndexRow = tableNPP.getSelectedRow();
        NhaPhanPhoi c = nhaPhanPhois.get(selectedIndexRow);
        if (selectedIndexRow == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng dữ liệu để xoá!");
        } else {
            int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá?");
            if (reply == JOptionPane.YES_OPTION) {
                new NhaPP_DAO().delete(c.getId());
                JOptionPane.showMessageDialog(this, "Xoá thành công!");
                fillDataToTable();
            } else {
                return;
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            selectedIndexRow = tableNPP.getSelectedRow();
            NhaPhanPhoi n = nhaPhanPhois.get(selectedIndexRow);
            if (validateForm() == true) {
                int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn sửa?");
                if (reply == JOptionPane.YES_OPTION) {
                    new NhaPP_DAO().delete(n.getId());
                    NhaPhanPhoi npp = new NhaPhanPhoi();
                    npp.setMaNPP(txtMaNPP.getText());
                    npp.setTenNPP(txtTenNPP.getText());
                    npp.setDiaChi(txtDiaChi.getText());
                    npp.setSdt(txtSDT.getText());
                    npp.setEmail(txtEmail.getText());
                    new NhaPP_DAO().insert(npp);
                    JOptionPane.showMessageDialog(this, "Sửa thành công!");
                    fillDataToTable();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng dữ liệu để sửa!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

