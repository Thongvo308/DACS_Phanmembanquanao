    private void tableCVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCVMouseClicked
        // TODO add your handling code here:
        txtMaCV.setEnabled(false);
        selectedIndexRow = tableCV.getSelectedRow();
        txtMaCV.setText(model.getValueAt(selectedIndexRow, 1).toString());
        txtTenCV.setText(model.getValueAt(selectedIndexRow, 2).toString());
    }//GEN-LAST:event_tableCVMouseClicked

    private void resetForm() {
        txtMaCV.setText("");
        txtTenCV.setText("");
        txtMaCV.setEnabled(true);
    }
    
    private void fillDataToTable() {
        chucvu = new ChucVu_DAO().getListChucVu();
        model.setRowCount(0);
        for (ChucVu c : chucvu) {
            model.addRow(new Object[]{
                tableCV.getRowCount() + 1, c.getMaCV(), c.getTenCV()
            });
        }
    }
    
