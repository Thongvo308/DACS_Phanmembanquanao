    private void btnThem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThem2ActionPerformed
        // TODO add your handling code here:
        try {
            if (txtMaCV.getText().equals("") || txtTenCV.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập đầy đủ thông tin!");
            } else {
                ChucVu c = new ChucVu();
                c.setMaCV(txtMaCV.getText());
                c.setTenCV(txtTenCV.getText());
                new ChucVu_DAO().insert(c);
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                fillDataToTable();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThem2ActionPerformed

    private void btnXoa2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoa2ActionPerformed
        // TODO add your handling code here:
        selectedIndexRow = tableCV.getSelectedRow();
        ChucVu c = chucvu.get(selectedIndexRow);
        if (selectedIndexRow == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng dữ liệu để xoá!");
        } else {
            int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá?");
            if (reply == JOptionPane.YES_OPTION) {
                new ChucVu_DAO().delete(c.getId());
                JOptionPane.showMessageDialog(this, "Xoá thành công!");
                fillDataToTable();
            } else {
                return;
            }
        }
    }//GEN-LAST:event_btnXoa2ActionPerformed

    private void btnSua2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSua2ActionPerformed
        // TODO add your handling code here:
        try {
            selectedIndexRow = tableCV.getSelectedRow();
            ChucVu c = chucvu.get(selectedIndexRow);
            if (txtTenCV.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập đầy đủ thông tin!");
            } else {
                int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn sửa?");
                if (reply == JOptionPane.YES_OPTION) {
                    new ChucVu_DAO().delete(c.getId());
                    ChucVu cv = new ChucVu();
                    cv.setMaCV(txtMaCV.getText());
                    cv.setTenCV(txtTenCV.getText());
                    new ChucVu_DAO().insert(cv);
                    JOptionPane.showMessageDialog(this, "Sửa thành công!");
                    fillDataToTable();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng dữ liệu để sửa!");
        }     
    }//GEN-LAST:event_btnSua2ActionPerformed

