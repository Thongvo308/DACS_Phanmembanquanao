    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String name = txtTimKiem.getText();
        if (name.length() == 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin tìm kiếm!");
        } else if (name.length() > 0) {
            sanpham = new SanPham_DAO().findByName(name);
            model.setRowCount(0);
            for (SanPham s : sanpham) {
                model.addRow(new Object[]{
                    tableSP.getRowCount() + 1, s.getMaSP(), s.getTenSP(), s.getLoaiSP().getTenLSP(), s.getHangSX().getTenHSX(), s.getKichCo(), s.getMauSac(), s.getSoluong(), s.getGiaNhap(), s.getGiaBan()
                });
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void setModel(SanPham s) {
        for (HangSanXuat hsx : daoHSX.getListHangSX()) {
            if (hsx.getId() == s.getHangSX().getId()) {
                for (int i = 0; i < cbxHangSX.getItemCount(); i++) {
                    if (hsx.getTenHSX().equals(String.valueOf(cbxHangSX.getItemAt(i)))) {
                        cbxHangSX.setSelectedIndex(i);
                    }
                }
            }
        }
        for (LoaiSanPham lsp : daoLSP.getListLoaiSP()){
            if (lsp.getId() == s.getLoaiSP().getId()) {
                for (int i = 0; i < cbxLoaiSP.getItemCount(); i++) {
                    if (lsp.getTenLSP().equals(String.valueOf(cbxLoaiSP.getItemAt(i)))) {
                        cbxLoaiSP.setSelectedIndex(i);
                    }
                }
            }
        }
    }

    private void fillDataToTable() {
        sanpham = new SanPham_DAO().getListAllSanPham();
        model.setRowCount(0);
        for (SanPham s : sanpham) {
            model.addRow(new Object[]{
                tableSP.getRowCount() + 1, s.getMaSP(), s.getTenSP(), s.getLoaiSP().getTenLSP(), s.getHangSX().getTenHSX(), s.getKichCo(), s.getMauSac(), s.getSoluong(), s.getGiaNhap(), s.getGiaBan()
            });
        }
    }

    private void resetForm() {
        txtMaSP.setText("");
        txtTenSP.setText("");
        txtKichCo.setText("");
        txtMauSac.setText("");
        txtSoLuong.setText("");
        txtGiaBan.setText("");
        txtGiaNhap.setText("");
        txtTimKiem.setText("");
        txtMaSP.setEnabled(true);
    }

    private boolean validateForm() {
        if (txtMaSP.getText().equals("") || txtTenSP.getText().equals("")
                || txtKichCo.getText().equals("") || txtMauSac.getText().equals("")
                || txtSoLuong.getText().equals("") || txtGiaNhap.getText().equals("") || txtGiaBan.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhâp đầy đủ thông tin!");
            return false;
        }
        try {
            Integer.parseInt(txtSoLuong.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số, không được chứa ký tự!");
            return false;
        }
        try {
            Integer.parseInt(txtGiaNhap.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá nhập phải là số, không được chứa ký tự!");
            return false;
        }
        try {
            Integer.parseInt(txtGiaBan.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá bán phải là số, không được chứa ký tự!");
            return false;
        }
        return true;
    }

