    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            if (validateForm() == true) {
                SanPham s = new SanPham();
                s.setMaSP(txtMaSP.getText());
                s.setTenSP(txtTenSP.getText());
                LoaiSanPham lsp = daoLSP.getByName(cbxLoaiSP.getSelectedItem().toString());
                s.setLoaiSP(lsp);
                HangSanXuat hsx = daoHSX.getByName(cbxHangSX.getSelectedItem().toString());
                s.setHangSX(hsx);
                s.setKichCo(txtKichCo.getText());
                s.setMauSac(txtMauSac.getText());
                s.setSoluong(Integer.parseInt(txtSoLuong.getText()));
                s.setGiaNhap(Integer.parseInt(txtGiaNhap.getText()));
                s.setGiaBan(Integer.parseInt(txtGiaBan.getText()));
                new SanPham_DAO().insert(s);
                JOptionPane.showMessageDialog(this, "Thêm thành công!");
                fillDataToTable();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void tableSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSPMouseClicked
        // TODO add your handling code here:
        txtMaSP.setEnabled(false);
        selectedIndexRow = tableSP.getSelectedRow();
        txtMaSP.setText(model.getValueAt(selectedIndexRow, 1).toString());
        txtTenSP.setText(model.getValueAt(selectedIndexRow, 2).toString());
        int position = tableSP.rowAtPoint(evt.getPoint());
        idSP = Integer.parseInt(tableSP.getValueAt(position, 0).toString());
        SanPham sp = daoSP.getByID(idSP);
        if (sp != null) {
            setModel(sp);
        }
        txtKichCo.setText(model.getValueAt(selectedIndexRow, 5).toString());
        txtMauSac.setText(model.getValueAt(selectedIndexRow, 6).toString());
        txtSoLuong.setText(model.getValueAt(selectedIndexRow, 7).toString());
        txtGiaNhap.setText(model.getValueAt(selectedIndexRow, 8).toString());
        txtGiaBan.setText(model.getValueAt(selectedIndexRow, 9).toString());
    }//GEN-LAST:event_tableSPMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        selectedIndexRow = tableSP.getSelectedRow();
        SanPham s = sanpham.get(selectedIndexRow);
        if (selectedIndexRow == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng dữ liệu để xoá!");
        } else {
            int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xoá?");
            if (reply == JOptionPane.YES_OPTION) {
                new SanPham_DAO().delete(s.getId());
                JOptionPane.showMessageDialog(this, "Xoá thành công!");
                fillDataToTable();
            } else {
                return;
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            selectedIndexRow = tableSP.getSelectedRow();
            SanPham s = sanpham.get(selectedIndexRow);
            if (validateForm() == true) {
                int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn sửa?");
                if (reply == JOptionPane.YES_OPTION) {
                    new SanPham_DAO().delete(s.getId());
                    SanPham sp = new SanPham();
                    sp.setMaSP(txtMaSP.getText());
                    sp.setTenSP(txtTenSP.getText());
                    LoaiSanPham lsp = daoLSP.getByName(cbxLoaiSP.getSelectedItem().toString());
                    sp.setLoaiSP(lsp);
                    HangSanXuat hsx = daoHSX.getByName(cbxHangSX.getSelectedItem().toString());
                    sp.setHangSX(hsx);
                    sp.setKichCo(txtKichCo.getText());
                    sp.setMauSac(txtMauSac.getText());
                    sp.setSoluong(Integer.parseInt(txtSoLuong.getText()));                 
                    sp.setGiaNhap(Integer.parseInt(txtGiaNhap.getText()));
                    sp.setGiaBan(Integer.parseInt(txtGiaBan.getText()));
                    new SanPham_DAO().insert(sp);
                    JOptionPane.showMessageDialog(this, "Sửa thành công!");
                    fillDataToTable();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng dữ liệu để sửa!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

